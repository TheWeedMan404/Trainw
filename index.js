function setupScrollAnimations() {
  const revealTargets = document.querySelectorAll('.who-card, .how-step, .price-card, .replace-col, .stat-item');
  if (typeof window.IntersectionObserver === 'function') {
    const observer = new IntersectionObserver(entries => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('section-reveal');
          observer.unobserve(entry.target);
        }
      });
    }, { threshold: 0.1 });

    revealTargets.forEach(el => {
      el.style.opacity = '0';
      observer.observe(el);
    });

    const animateCounter = (el, target, duration = 1200) => {
      const start = performance.now();
      const parsed = parseInt(target, 10);
      if (Number.isNaN(parsed)) return;
      const update = now => {
        const progress = Math.min((now - start) / duration, 1);
        const ease = 1 - Math.pow(1 - progress, 3);
        el.textContent = String(Math.floor(ease * parsed));
        if (progress < 1) {
          requestAnimationFrame(update);
        } else {
          el.textContent = target;
        }
      };
      requestAnimationFrame(update);
    };

    const statObserver = new IntersectionObserver(entries => {
      entries.forEach(entry => {
        if (!entry.isIntersecting) return;
        const value = entry.target.querySelector('.stat-val');
        if (value) animateCounter(value, value.textContent);
        statObserver.unobserve(entry.target);
      });
    }, { threshold: 0.5 });

    document.querySelectorAll('.stat-item').forEach(el => statObserver.observe(el));
  } else {
    revealTargets.forEach(el => el.classList.add('section-reveal'));
  }
}

