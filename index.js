function detectLang() {
  const saved = localStorage.getItem('trainw_lang');
  if (saved && T[saved]) return saved;
  const bl = (navigator.language || 'fr').toLowerCase();
  if (bl.startsWith('ar')) return 'ar';
  if (bl.startsWith('en')) return 'en';
  return 'fr';
}

let lang = detectLang();

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (T[lang]?.[key] !== undefined) el.textContent = T[lang][key];
  });
  if (lang === 'ar') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.documentElement.setAttribute('lang', 'ar');
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.documentElement.setAttribute('lang', lang);
  }
}

function setLang(l) {
  lang = l;
  localStorage.setItem('trainw_lang', l);
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
  document.querySelector(`.lang-btn[data-lang="${l}"]`)?.classList.add('active');
  applyTranslations();
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => setLang(btn.dataset.lang));
});

document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
document.querySelector(`.lang-btn[data-lang="${lang}"]`)?.classList.add('active');

window.addEventListener('scroll', () => {
  document.getElementById('navbar').style.borderBottomColor =
    window.scrollY > 10 ? 'var(--bd)' : 'transparent';
});

applyTranslations();
